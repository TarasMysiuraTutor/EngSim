/**
 * –û—Ç—Ä–∏–º–∞—Ç–∏ thumbnail - –∑ Cloudinary –∞–±–æ fallback –Ω–∞ YouTube
 * –ü—ñ–¥—Ç—Ä–∏–º—É—î –±–∞–≥–∞—Ç–æ–º–æ–≤–Ω—ñ thumbnails
 */
export const getVideoThumbnail = (video, lang = 'en') => {
  if (video.thumbnail) {
    // –Ø–∫—â–æ thumbnail - –æ–±'—î–∫—Ç –∑ –º–æ–≤–∞–º–∏
    if (typeof video.thumbnail === 'object' && !Array.isArray(video.thumbnail)) {
      return video.thumbnail[lang] || video.thumbnail['en'] || Object.values(video.thumbnail)[0];
    }
    // –Ø–∫—â–æ thumbnail - –ø—Ä–æ—Å—Ç–æ —Ä—è–¥–æ–∫
    return video.thumbnail;
  }
  // Fallback –Ω–∞ YouTube thumbnail
  return `https://i.ytimg.com/vi/${video.youtubeId}/hqdefault.jpg`;
};

/**
 * –ö–æ–Ω–≤–µ—Ä—Ç—É–≤–∞—Ç–∏ Google Drive –ø–æ—Å–∏–ª–∞–Ω–Ω—è —É direct download
 */
export const convertGoogleDriveUrl = (url) => {
  // –§–æ—Ä–º–∞—Ç: https://drive.google.com/file/d/FILE_ID/view?usp=sharing
  // –ö–æ–Ω–≤–µ—Ä—Ç—É—î–º–æ –≤: https://drive.google.com/uc?export=download&id=FILE_ID
  
  const match = url.match(/\/d\/([^/]+)/);
  if (match && match[1]) {
    return `https://drive.google.com/uc?export=download&id=${match[1]}`;
  }
  return url; // –ü–æ–≤–µ—Ä—Ç–∞—î–º–æ –æ—Ä–∏–≥—ñ–Ω–∞–ª—å–Ω–∏–π —è–∫—â–æ –Ω–µ Google Drive
};

/**
 * –ó–∞–≤–∞–Ω—Ç–∞–∂–∏—Ç–∏ —ñ–Ω—Å—Ç—Ä—É–∫—Ü—ñ—é
 */
export const downloadInstruction = (video, lang) => {
  const instruction = video.instructions?.[lang];
  if (!instruction) {
    console.warn(`–Ü–Ω—Å—Ç—Ä—É–∫—Ü—ñ—è –¥–ª—è –º–æ–≤–∏ ${lang} –Ω–µ –∑–Ω–∞–π–¥–µ–Ω–∞`);
    return;
  }

  // –ö–æ–Ω–≤–µ—Ä—Ç—É—î–º–æ Google Drive URL —è–∫—â–æ –ø–æ—Ç—Ä—ñ–±–Ω–æ
  const downloadUrl = instruction.url.includes('drive.google.com') 
    ? convertGoogleDriveUrl(instruction.url)
    : instruction.url;

  // –í—ñ–¥–∫—Ä–∏–≤–∞—î–º–æ –≤ –Ω–æ–≤–æ–º—É –≤—ñ–∫–Ω—ñ –¥–ª—è –∑–∞–≤–∞–Ω—Ç–∞–∂–µ–Ω–Ω—è
  window.open(downloadUrl, '_blank');
};

/**
 * –ó–∞–≤–∞–Ω—Ç–∞–∂–∏—Ç–∏ —Ä–µ—Å—É—Ä—Å (–º–æ–¥–µ–ª—å, –∫—Ä–µ—Å–ª–µ–Ω–Ω—è)
 */
export const downloadResource = (resource) => {
  const link = document.createElement('a');
  link.href = resource.url;
  link.download = resource.filename;
  link.target = '_blank';
  document.body.appendChild(link);
  link.click();
  document.body.removeChild(link);
};

/**
 * –ü–µ—Ä–µ–≤—ñ—Ä–∏—Ç–∏ —á–∏ —î —ñ–Ω—Å—Ç—Ä—É–∫—Ü—ñ—è –¥–ª—è –º–æ–≤–∏
 */
export const hasInstruction = (video, lang) => {
  return video.instructions && video.instructions[lang];
};

/**
 * –ü–µ—Ä–µ–≤—ñ—Ä–∏—Ç–∏ —á–∏ —î –¥–æ–¥–∞—Ç–∫–æ–≤—ñ —Ä–µ—Å—É—Ä—Å–∏
 */
export const hasResources = (video) => {
  return video.resources && video.resources.length > 0;
};

